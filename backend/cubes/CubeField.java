package main.java.com.cubes;

public class CubeField {
    private String fieldName;
    private String fieldVE;
    private String hierarchy;
    private Integer hierarchyOrder;

    public String getFieldName() {
        return fieldName;
    }

    public void setFieldName(String fieldName) {
        this.fieldName = fieldName;
    }

    public String getFieldVE() {
        return fieldVE;
    }

    public void setFieldVE(String fieldVE) {
        this.fieldVE = fieldVE;
    }

    public String getHierarchy() {
        return hierarchy;
    }

    public void setHierarchy(String hierarchy) {
        this.hierarchy = hierarchy;
    }

    public Integer getHierarchyOrder() {
        return hierarchyOrder;
    }

    public void setHierarchyOrder(Integer hierarchyOrder) {
        this.hierarchyOrder = hierarchyOrder;
    }
}
